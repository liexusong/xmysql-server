package schemas

//定义MySQL的information_schemas 用于管理各个
type InfoSchemas interface {

	//获取数据库对象
	GetSchemaByName(schemaName string) (Database, error)

	//判断数据库是否存在
	GetSchemaExist(schemaName string) bool

	GetTableByName(schema string, tableName string) (Table, error)

	GetTableExist(schemaName string, tableName string) bool

	GetAllSchemaNames() []string

	GetAllSchemas() []Database

	GetAllSchemaTablesByName(schemaName string) []Table

	PutDatabaseCache(datacache Database)
}
